package cn.itcast.springboot.mybatis.vo;

public class Message {

	private Object date;

	private boolean Success;

	

	public Object getDate() {
		return date;
	}

	public void setDate(Object date) {
		this.date = date;
	}

	public boolean isSuccess() {
		return Success;
	}

	public void setSuccess(boolean success) {
		Success = success;
	}

}
